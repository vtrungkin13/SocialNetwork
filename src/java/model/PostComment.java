package model;

import java.sql.Date;
import java.sql.Time;

/**
 *
 * @author vtrun
 */
public class PostComment {

    private User user;
    private Post post;
    private Date date;
    private Time time;
    private String comment;

    public PostComment() {
    }

    public PostComment(User user, Post post, Date date, Time time, String comment) {
        this.user = user;
        this.post = post;
        this.date = date;
        this.time = time;
        this.comment = comment;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }

    public Post getPost() {
        return post;
    }

    public void setPost(Post post) {
        this.post = post;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public Time getTime() {
        return time;
    }

    public void setTime(Time time) {
        this.time = time;
    }

    public String getComment() {
        return comment;
    }

    public void setComment(String comment) {
        this.comment = comment;
    }

}
