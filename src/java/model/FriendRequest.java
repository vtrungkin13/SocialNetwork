package model;

/**
 *
 * @author tungb
 */
public class FriendRequest {
    private User from;
    private User to;

    public FriendRequest() {
    }

    public FriendRequest(User from, User to) {
        this.from = from;
        this.to = to;
    }

    public User getFrom() {
        return from;
    }

    public void setFrom(User from) {
        this.from = from;
    }

    public User getTo() {
        return to;
    }

    public void setTo(User to) {
        this.to = to;
    }
    
}
